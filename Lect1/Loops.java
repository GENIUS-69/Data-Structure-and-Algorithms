public class Loops {
  public static void main(String[] args) {
    int a = 1;
    while (a!=5) {
      System.out.println("Hello Genius");
      a++;
    }
    // Use while when we don't know how many time the loop is going to run
    
    for (int i = 0; i < 10; i++) {
      System.out.println(i);
    }
    // Use for when we know how many time the loop is going to run
  }
}
